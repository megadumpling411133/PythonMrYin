a = [1000, 80, 100, 90]
最大值 = a[0]
for x in a:
    print(f'目前讀取:  {x}')
    if x > 最大值:
        最大值 = x
print(f"最大值為: {最大值}")

print('=============================')
最小值 = a[0]
for x in a:
    print(f'目前讀取:  {x}')
    if x < 最小值:
        最小值 = x
print(f"最小值為: {最小值}")
print('=============================')

加總 = 0

for x in a:
    print(f'目前讀取:  {x}')
    加總 += x
print(f"加總值: {加總}")
print('=============================')

